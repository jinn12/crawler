package com.kiprisplus.api.trademark.model.vo.bibliographyDetailInfoSearch.item;

public class ApplicantInfoVO {
	private String applicantAddress;
	private String applicantCode;
	private String nameKoreanLong;
	private String nationalCode;
	private String seq;
	public String getApplicantAddress() {
		return applicantAddress;
	}
	public void setApplicantAddress(String applicantAddress) {
		this.applicantAddress = applicantAddress;
	}
	public String getApplicantCode() {
		return applicantCode;
	}
	public void setApplicantCode(String applicantCode) {
		this.applicantCode = applicantCode;
	}
	public String getNameKoreanLong() {
		return nameKoreanLong;
	}
	public void setNameKoreanLong(String nameKoreanLong) {
		this.nameKoreanLong = nameKoreanLong;
	}
	public String getNationalCode() {
		return nationalCode;
	}
	public void setNationalCode(String nationalCode) {
		this.nationalCode = nationalCode;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}


}
