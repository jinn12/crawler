package com.kiprisplus.api.patent.model.vo.bibliographyDetailInfoSearch.item;

public class LegalStatusInfoVO {
	private String commonCodeName;
	private String documentEngName;
	private String documentName;
	private String receiptDate;
	private String receiptNumber;
	public String getCommonCodeName() {
		return commonCodeName;
	}
	public void setCommonCodeName(String commonCodeName) {
		this.commonCodeName = commonCodeName;
	}
	public String getDocumentEngName() {
		return documentEngName;
	}
	public void setDocumentEngName(String documentEngName) {
		this.documentEngName = documentEngName;
	}
	public String getDocumentName() {
		return documentName;
	}
	public void setDocumentName(String documentName) {
		this.documentName = documentName;
	}
	public String getReceiptDate() {
		return receiptDate;
	}
	public void setReceiptDate(String receiptDate) {
		this.receiptDate = receiptDate;
	}
	public String getReceiptNumber() {
		return receiptNumber;
	}
	public void setReceiptNumber(String receiptNumber) {
		this.receiptNumber = receiptNumber;
	}


}
